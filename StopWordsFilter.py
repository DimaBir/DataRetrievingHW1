# Removes all stop words from text
# params: text - contains text as string that must be filtered
# returns: set of strings without stop words
def FilterStopWords(text):

    # Transform collection of stop words to set
    stop_words = set(stopWordsEnglish)
    
    # Transform text to set of strings
    word_tokens = text.split()

    # Init result set
    filtered_sentence = []

    # For esch word from text check if it is a stop word, clear stopr words
    for w in word_tokens:
        if w not in stop_words:
            filtered_sentence.append(w)

    return set(filtered_sentence)


